class Solution:
    def longestCommonPrefix(self, strs: List[str]) -> str:
        if (len(strs) == 0 ):
            return "Empty list"
        else:
            shortest_str = min(strs,key=len)
            for i, char in enumerate(shortest_str):
                for other in strs:
                    if other[i] != char:
                     return shortest_str[:i]
                    else:
                        print(other[i])
                        pass
        return shortest_str

